    id BIGSERIAL PRIMARY KEY,
    hero_enabled BOOLEAN NOT NULL DEFAULT TRUE,
    hero_title VARCHAR(255),
    hero_subtitle VARCHAR(1000),
    hero_button_text VARCHAR(120),
    hero_button_link VARCHAR(500),
    hero_content_position VARCHAR(30) NOT NULL DEFAULT 'LEFT',
    hero_image_url VARCHAR(500),
    hero_text_color_mode VARCHAR(30) NOT NULL DEFAULT 'AUTO',
    hero_custom_text_color VARCHAR(20),
    hero_overlay_strength INTEGER NOT NULL DEFAULT 42,
