    value_title VARCHAR(255),
    value_description VARCHAR(1000),
    value_cards_json TEXT NOT NULL DEFAULT '[]',
    about_enabled BOOLEAN NOT NULL DEFAULT TRUE,
    about_eyebrow VARCHAR(120),
    about_title VARCHAR(255),
    about_description VARCHAR(1000),
    about_button_text VARCHAR(120),
    cta_enabled BOOLEAN NOT NULL DEFAULT TRUE,
    cta_title VARCHAR(255),
    cta_description VARCHAR(1000),
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
    CONSTRAINT chk_homepage_configs_singleton CHECK (id = 1)
);

